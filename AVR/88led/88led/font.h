//	ch : 文字コード(sjis), font : フォントのアドレスを格納, return : 文字のバイト数
uint8_t fchar (char *ch, uint8_t **font)
{
	//1バイト文字
	//数字
	#ifdef USEFONT_NUM
	if ((ch[0] >= '0') && (ch[0] <= '9'))
	{
		*font = fnum + ch[0] - '0';
		return 1;
	}
	#endif //USEFONT_NUM
	
	//大文字アルファベット
	#ifdef USEFONT_UPALP
	if ((ch[0] >= 'A') && (ch[0] <= 'Z'))
	{
		*font = fupalp + ch[0] - 'A';
		return 1;
	}		
	#endif //USEFONT_UPALP
	
	//小文字アルファベット
	#ifdef USEFONT_LOWALP
	if ((ch[0] >= 'a') && (ch[0] <= 'z'))
	{
		*font = flowalp + ch[0] - 'a';
		return 1;
	}		
	#endif //USEFONT_LOWALP
	
	//2バイト文字
	uint16_t wch = ((ch[0] << 8) | ch[1]);
	
	//ひらがな
	#ifdef USEFONT_HIRA
	if ((wch >= BEGIN_HIRA) && (wch <= END_HIRA))
	{
		*font = fhira + (uint8_t)(wch - BEGIN_HIRA);
		return 2;
	}		
	#endif //USEFONT_HIRA
	
	//カタカナ1
	#ifdef USEFONT_KANA1
	if ((wch >= BEGIN_KANA1) && (wch <= END_KANA1))
	{
		*font = fkana1 + (uint8_t)(wch - BEGIN_KANA1);
		return 2;
	}		
	#endif //USEFONT_KANA1
	
	//カタカナ2
	#ifdef USEFONT_KANA2
	if ((wch >= BEGIN_KANA2) && (wch <= END_KANA2))
	{
		*font = fkana2 + (uint8_t)(wch - BEGIN_KANA2);
		return 2;
	}		
	#endif //USEFONT_KANA2
	
	return 0;
}

